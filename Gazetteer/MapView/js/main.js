var map = AmCharts.makeChart("mapdiv", {
                             type: "map",
                             pathToImages: "http://www.amcharts.com/lib/3/images/",
                             panEventsEnabled : true,
                             areasSettings: {
                             autoZoom: true,
                             color: "#FFFFFF",
                             outlineColor: "#EFEFF4",
                             selectedColor: "#2ABA2B",
                             rollOverColor: "#FFFFFF",
                             rollOverOutlineColor: "#EFEFF4"
                             },
                             dataProvider: continentsDataProvider
                             });

var mapObject;
var previousMapObject;

function handleGoHome() {    
    map.dataProvider = continentsDataProvider;
    previousMapObject = null;
    mapObject = "";
    $("#sidebar-1").empty();
    $("#mapTitle").html("World");
    loadMapViewReport();
    document.getElementById('backButton').style.display = "none";
    map.validateNow();
}

function homeButtonClicked(event){
    handleGoHome();
}

function handleMapObjectClick(event) {
    mapObject = event.mapObject;
    $("#sidebar-1").empty();
    loadMapViewReport();
    document.getElementById('backButton').style.display = "block";
    $("#mapTitle").html(mapObject.title);
}

map.addListener("dataUpdated", function (event) {
                if (mapObject) {
                previousMapObject = mapObject;
                var area = map.getObjectById(mapObject.id);
                if (area) {
                area.color = '#2ABA2B';
                area.colorReal = area.color;
                // make the chart take in new color
                map.returnInitialColor(area);
                }
                } 
                })


var pZoom = 0;
map.addListener("zoomCompleted", function (event) {
                zoomLevel = map.zoomLevel();
                if ( pZoom == zoomLevel ) {
                return;
                }
                pZoom = zoomLevel;
                if (zoomLevel == 1) {
                handleGoHome();
                $("#mapTitle").html("World");
                }
                if (previousMapObject && previousMapObject.id != mapObject.id) {
                var area = map.getObjectById(previousMapObject.id);
                if (area) {
                area.color = '#FFFFFF';
                area.colorReal = area.color;
                // make the chart take in new color
                map.returnInitialColor(area);
                }
                previousMapObject = null;
                }
                })



// monitor when home icon was clicked and also go to continents map
map.zoomControl.zoomControlEnabled = false;
map.zoomControl.panControlEnabled = false;
map.addListener("homeButtonClicked", handleGoHome);
map.addListener("clickMapObject", handleMapObjectClick);